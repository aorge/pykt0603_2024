import tensorflow as tf

v1 = [3.0, 1.0, -1.0, -3.0, 2.4, 5.9, 0.001, -0.001, 0, 8.5, 0.0000000001, -0.0000000001]

r1 = tf.nn.relu(v1)
print(r1)
print('------------------------------------------------')
r2 = tf.nn.sigmoid(v1)
print(r2)
print('------------------------------------------------')
v2 = [3.0, 4.0, 5.0, 6.0, 7.0, 8.0, 9.0]
r3 = tf.nn.softmax(v2)
print(r3)
print('------------------------------------------------')